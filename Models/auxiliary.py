from abc import ABC, abstractmethod


#! ==================================================================================== #
#! =================================== Base Models ==================================== #
class ML_Model(ABC):
    @abstractmethod
    def __init__(self):
        pass

    #*____________________________________________________________________________________ #
    @abstractmethod
    def process_data(self):
        pass

    #*____________________________________________________________________________________ #
    @abstractmethod
    def fit(self):
        pass
    
    #*____________________________________________________________________________________ #
    @abstractmethod
    def predict(self):
        pass

